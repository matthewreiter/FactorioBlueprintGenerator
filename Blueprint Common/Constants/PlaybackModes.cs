namespace BlueprintCommon.Constants
{
    public static class PlaybackModes
    {
        /// <summary>
        /// Sounds created by the speaker will be heard within the audible range of the speaker.
        /// </summary>
        public const string Local = "local";

        /// <summary>
        /// Sounds created by the speaker will be heard everywhere on the speaker's surface.
        /// </summary>
        public const string Surface = "surface";

        /// <summary>
        /// Sounds created by the speaker will be heard everywhere.
        /// </summary>
        public const string Global = "global";
    }
}
